# Write a method that takes a string and returns the number of vowels
# in the string. You may assume that all the letters are lower cased.
# You can treat "y" as a consonant.
#
# Difficulty: easy.

# in: string
# out: integer, representing the number of vowels in the string
# - need my vowels
# - need to count the number of times they occur within the string (#count should do this nicely)
# - alternative strategy would be to iterate through each letter, and see if vowels include the passed char

def count_vowels(string)
  string.downcase.count('aeiou')
end

